public class Aps {
  String pwd;
  String ssid;
  String apRefId;
  String pwdId;

  public String getPwd() {
    return pwd;
  }

  public void setPwd(String pwd) {
    this.pwd = pwd;
  }

  public String getSsid() {
    return ssid;
  }

  public void setSsid(String ssid) {
    this.ssid = ssid;
  }

  public String getApRefId() {
    return apRefId;
  }

  public void setApRefId(String apRefId) {
    this.apRefId = apRefId;
  }

  public String getPwdId() {
    return pwdId;
  }

  public void setPwdId(String pwdId) {
    this.pwdId = pwdId;
  }
}
